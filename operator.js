// Arithmetic Operators
let a = 10;
let b = 3;

console.log("Addition:", a + b);           // 13
console.log("Subtraction:", a - b);        // 7
console.log("Multiplication:", a * b);     // 30
console.log("Division:", a / b);           // 3.33...
console.log("Modulus:", a % b);            // 1
console.log("Exponentiation:", a ** b);    // 1000

// Assignment Operators
let x = 5;
x += 2; // x = x + 2
console.log("After += 2:", x);             // 7

x *= 3;
console.log("After *= 3:", x);             // 21

x -= 4;
console.log("After -= 4:", x);             // 17

x /= 2;
console.log("After /= 2:", x);             // 8.5

//  Comparison Operators
console.log("a == b:", a == b);            // false
console.log("a != b:", a != b);            // true
console.log("a === b:", a === b);          // false
console.log("a > b:", a > b);              // true
console.log("a < b:", a < b);              // false
console.log("a >= b:", a >= b);            // true
console.log("a <= b:", a <= b);            // false

// Logical Operators
let isAdult = true;
let hasVoterID = false;

console.log("AND (&&):", isAdult && hasVoterID);  // false
console.log("OR (||):", isAdult || hasVoterID);   // true
console.log("NOT (!):", !isAdult);                // false

// Increment / Decrement
let count = 0;
console.log("Initial:", count);      // 0
console.log("Post-Increment:", count++); // 0 (then becomes 1)
console.log("After Post-Increment:", count); // 1

console.log("Pre-Decrement:", --count); // 0

// Ternary Operator
let age = 20;
let result = (age >= 18) ? "Adult" : "Minor";
console.log("Ternary Result:", result);  // Adult









